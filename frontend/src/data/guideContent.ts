export interface PromptExample {
  /** 示例唯一 id */
  id: string
  /** 示例标题：场景 + 风格 + 关键元素 */
  title: string
  /** 适用场景简述 */
  scenario: string
  /** Prompt 原文（来自 Creatok 验证过的高质量提示词） */
  rawPrompt: string
  /** 是否含 @图片N 引用 */
  hasImageRef: boolean
  /** 参考图引用说明：上传几张？分别是什么？@图片N 怎么对应？ */
  imageRefHint?: string
  /** 标签：风格/比例/时长/参考图数量等，方便筛选 */
  tags: string[]
}

export interface ModuleGuide {
  /** 锚点 id（与左侧目录联动） */
  anchor: string
  /** 模块名 */
  title: string
  /** Element Plus 图标名（动态组件用） */
  icon: string
  /** 品牌渐变 class（gradient-purple 等，见 global.css） */
  gradient: string
  /** 一句话定位：这个模块是干啥的 */
  intro: string
  /** 分段说明：每段一个主题 */
  sections: Array<{
    subtitle: string
    tips: string[]
  }>
}

